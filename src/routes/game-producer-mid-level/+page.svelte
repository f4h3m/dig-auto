						<h4>Game Producer</h4>
						<p>
							The role of the producer is to drive quality targets and own production schedules.
							This is achieved by working closely with various departments to identify needs,
							applying resources efficiently and appropriately through a monthly Sprint framework.
							The role requires candidates to be excellent and persuasive communicators, capable of
							coordinating with a wide variety of internal and external partners. The ideal
							candidate will be comfortable with all degrees of ownership, from personally leading
							to pro-active planning. Production leaders must thrive in an ambitious and sometimes
							ambiguous environment, and be adept at driving quality in line with team, schedule,
							and budget.
						</p>
						<h4>Responsibilities:</h4>
						<ul>
							<li>
								Work with the project leadership team to establish the strategy to meet milestones
								and important deliverables.
							</li>
							<li>
								Manage the development project team and maintain the overall focus and vision of the
								project
							</li>
							<li>
								Create and maintain project schedules and milestone documentation, monitoring the
								progress of milestones and specific tasks against those schedules
							</li>
							<li>
								Develop project indicators that track project quality, delivery, and predictability.
							</li>
							<li>
								Identification, assessment, and prioritization of risks followed by a coordinated
								application of resources to minimize and manage risks.
							</li>
							<li>Create and track monthly sprints.</li>
							<li>
								When required, act as liaison between internal and external partners to ensure
								smooth and effective communication.
							</li>
							<li>
								Understand and help plan the core game scope and vision while understanding how the
								player, community, and our influencer partners will engage with it
							</li>
							<li>
								Provide subject matter expertise on games production and development to Digital
								insight Games stakeholders
							</li>
							<li>
								Stay on top of deliverables, asset previews, feedback, and other aspects to ensure
								the project plan remains on track
							</li>
							<li>
								Maintain knowledge of industry trends, competitors, and tools to help us stay at the
								cutting edge of development
							</li>
						</ul>
						<h4>Requirements:</h4>
						<ul>
							<li>
								5+ years of professional experience as a project manager, with experience in
								shipping AAA games.
							</li>
							<li>
								Skilled leader who can challenge and influence a variety of partners across all
								levels of organization.
							</li>
							<li>
								Excellent change management skills, i.e., comfortable with ambiguity and constant
								change, yet able to set priorities and work commitments.
							</li>
							<li>
								Strong project management fundamentals including capacity planning, scope assessment
								and management, and project milestone planning.
							</li>
							<li>
								Solid grasp of project management software (e.g. JIRA, Microsoft Project,
								Confluence, Hansoft, etc.).
							</li>
							<li>
								Strong understanding of software development and project management processes and
								practices
							</li>
							<li>
								Strong ability to work with various producers, developers and testers across
								multiple studios and projects Experience with Microsoft Office (emphasis on Excel),
								Hansoft, JIRA, Confluence and other project tracking and bug database software
							</li>
							<li>Leadership experience or a proven track record of delivering complex projects</li>
							<li>Strong communication skills (written and verbal)</li>
							<li>
								Excellent organizational & prioritization skills, attention to detail, process
								knowledge and ability to multitask
							</li>
						</ul>
