						<h4>Lead Visual Effects Artist</h4>
						<p>
							This important role requires knowledge of basic modeling, UV mapping, and texture &
							shader creation to cover a wide range of game content – from player skills to UI
							effects to environment objects. A successful candidate portfolio will show a strong
							sense of timing, visual composition, color, anticipation/follow through, and ability
							to communicate gameplay. Ultimately the goal is to attach stunning visual effects to
							the game and do so in a way that preserves framerate and memory.
						</p>
						<h4>Responsibilities:</h4>
						<ul>
							<li>
								Lead a team of visual effects artists to create beautiful run-time effects in Unreal
								5.
							</li>
							<li>
								Create effects yourself using Maya, Houdini, Niagra, After Effects and other similar
								tools.
							</li>
							<li>
								Place and sequence Unreal Blueprint effects in service of design and art needs.
							</li>
							<li>Troubleshoot artistic, technical, and performance issues.</li>
							<li>
								Collaborate with team members and production to ensure that project goals are met.
							</li>
						</ul>
						<h4>Requirements:</h4>
						<ul>
							<li>5+ years of experience in creating VFX for AAA games.</li>
							<li>
								A critical eye for the timing, animation, and motion of real-world objects and
								visual effects.
							</li>
							<li>
								Experience with a variety of techniques and tools for creating VFX animation
								(standard particle systems, fluid sims, physics sims, etc.)
							</li>
							<li>
								Ability to create all source art needed for VFX (textures, geometry, flipbooks,
								normal maps, vector field textures, etc.)
							</li>
							<li>
								Advanced understanding of complex node-based pixel/vertex shader networks used in
								Unreal Engine.
							</li>
							<li>
								Expert knowledge of profiling tools to diagnose and optimize in-game performance,
								including optimization & LOD versioning techniques.
							</li>
							<li>Proficient in Photoshop for painting and editing textures.</li>
						</ul>
