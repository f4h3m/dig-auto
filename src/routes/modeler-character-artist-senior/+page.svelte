						<h4>Senior Character Modeler</h4>
						<p>
							The senior character modeler is responsible for bringing fantastical creatures and
							characters to life with a photorealistic style. A strong understanding of realistic
							sculptural form, anatomy, and an ability to work in a wide range of artistic styles is
							required. A high attention to detail and strong technical and artistic problem-solving
							abilities are equally important for the position. The models could be built from
							scratch or based on templates, vendor models, photogrammetry scans, concept art, and
							actor reference.
						</p>
						<h4>Responsibilities:</h4>
						<ul>
							<li>
								Work with the team to create and maintain stunning digital characters for use in
								Unreal 5.
							</li>
							<li>
								Create realistic Characters and Creatures from concepts using the latest industry
								tools.
							</li>
							<li>Sculpt, model, UV, texture and create materials for characters and creatures.</li>
							<li>Use grooming techniques to implement hair and fur.</li>
							<li>
								Create realistic clothing to implement wardrobes based of concept art/reference.
							</li>
							<li>Optimize and deliver clean, organized assets.</li>
							<li>Communicate with Production to ensure all deadlines are met.</li>
						</ul>
						<h4>Requirements:</h4>
						<ul>
							<li>3+ years of experience in the game or film industry as a Character Modeler.</li>
							<li>Character and prop conceptual design skills.</li>
							<li>Strong skills creating textures and material creation within a PBR pipeline.</li>
							<li>Strong knowledge of human anatomy, clothing tools, re-meshing techniques.</li>
							<li>Ability to translate 2d artwork to 3d models/sculpts.</li>
						</ul>
