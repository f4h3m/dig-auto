						<h4>Audio Lead</h4>
						<p>
							The lead audio role is responsible for the creation, integration and mixing of all
							audio in the game. Ranging from composed, streaming compositions to run-time triggered
							sound-effects, the audio director collaborates with designers and directors to create
							a world class aural experience for players. <br />
							The role includes managing a small audio team of people, providing feedback and tasking
							and monitoring of activities within the team.
						</p>
						<h4>Responsibilities:</h4>
						<ul>
							<li>
								Establish and communicate the audio vision in cooperation with the Creative Director
								and Lead Designer.
							</li>
							<li>Compose music and sounds that enhance the gameplay experience.</li>
							<li>
								Manage the day-to-day operation of the project’s audio team, including ensuring
								submission of final audio files on or before required deadlines and troubleshooting
								and debugging of game audio systems
							</li>
							<li>
								Perform quality control review for the project’s audio on all applicable platforms
							</li>
							<li>Manage and lead all external audio resources when necessary.</li>
							<li>
								Assist with voice recording sessions and further editing and integration of those
								files.
							</li>
						</ul>
						<h4>Requirements:</h4>
						<ul>
							<li>5 years of experience as an audio designer in the gaming industry.</li>
							<li>Have shipped at least one AAA title in a Senior Audio Designer capacity.</li>
							<li>
								Comfortable troubleshooting and optimizing audio resources without frequent code
								support or intervention
							</li>
							<li>Good interpersonal communication skills.</li>
						</ul>
