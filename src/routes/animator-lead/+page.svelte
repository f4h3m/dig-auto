						<h4>Lead Animator</h4>
						<p>
							The role of lead animator is responsible for the planning and implementation of all
							animation within the project. From creating the animation tree to motion capture, the
							lead animator is focused on bringing animations into the game with a high visual
							fidelity. A successful lead animator will also collaborate with engineering, design
							and directors on gameplay mechanics, potential issues with those mechanics, and
							determine solutions.
						</p>
						<h4>Responsibilities:</h4>
						<ul>
							<li>
								Manage a team of animators, providing guidance on tasks, quality and technical
								aspects.
							</li>
							<li>
								Collaborate with the Creative and Art Director on animation targets and style.
							</li>
							<li>
								Create compelling animation sets with an emphasis on proper weight, balance and
								timing
							</li>
							<li>
								Build and maintain an animation schedule that works with the overall project
								milestones
							</li>
							<li>Identify potential areas of risk and work with other groups to mitigate them</li>
							<li>Partner with Production to direct outsourcers when necessary</li>
						</ul>
						<h4>Requirements:</h4>
						<ul>
							<li>5+ years of experience in the game industry</li>
							<li>At least 1 title shipped as a Lead Animator on a AAA title</li>
							<li>
								Great understanding of realistic body mechanics as well as animation principles
							</li>
							<li>Expert knowledge of Maya, specializing in rigging, skinning, or animating</li>
							<li>Experience with skeletal mesh animation systems</li>
						</ul>
