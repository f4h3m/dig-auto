						<p>
							The Lead UI/UX role is responsible for utilizing graphic design and animation to
							communicate functionality and game state to the player. A successful UI/UX artist will
							collaborate early with directors using clear wireframes, user flows, and sample
							prototypes, mockups, and iconography in both 3D and 2D.
						</p>
						<h4>Responsibilities:</h4>
						<ul>
							<li>
								Work closely with design and direction to create user interface elements (menus,
								widgets, icons) that translate game systems into intuitive interfaces that support
								and enhance the general game experience.
							</li>
							<li>
								Work with the UI team, and across disciplines, to design UI in accordance with art
								direction and systems design documents.
							</li>
							<li>
								Deliver assets for all stages of the UX/UI design process. From wireframes to
								in-game asset creation.
							</li>
							<li>
								Individually implement UI systems in Unreal 5, using Blueprints and other modules.
							</li>
						</ul>
