						<h4>Lighting Artist</h4>
						<p>
							The lighter is responsible for lighting the different environments of the game with
							realistic, cinematic lighting in a real-time environment, primarily using the tools of
							Unreal 5. An excellent eye for composition and color, a great understanding of light
							and shadow, and a strong ability to present games and cinematics at their highest
							level will be the primary focus. Collaboration with the Creative and Art Directors in
							addition to the Technical Directors will allow for best results. A strong
							understanding of color grading, PBR and Linear workflows is required.
						</p>
						<h4>Responsibilities:</h4>
						<ul>
							<li>
								Create believable and cinematic lighting scenarios for run-time game locations.
							</li>
							<li>
								Ensure the lighting in a location works within defined real-time rendering budgets.
							</li>
							<li>
								Work closely with the Creative Director and Art Director to bring artistic vision to
								life.
							</li>
							<li>Optimize lighting data for memory and frame.</li>
							<li>
								Engage in look-development around key surfaces and shaders to ensure lighting
								delivers against visual reference.
							</li>
							<li>Place and adjust light sources within the environment.</li>
							<li>
								Tweak shading materials and textures to improve the perception of the lighting.
							</li>
							<li>
								Contribute to the development and placement of special effects related to lighting
								and atmosphere, such as light flares, fog, god rays, and camera optics.
							</li>
							<li>Work with level designers to ensure integration with gameplay.</li>
						</ul>
						<h4>Requirements:</h4>
						<ul>
							<li>
								5+ years proven industry experience in Games or Film in generating final
								lighting/rendering.
							</li>
							<li>
								Deep understanding of CG lighting technology, both real-time and offline rendering.
							</li>
							<li>
								Thorough understanding of direct/indirect lighting, color space, color theory,
								exposure, HDR lighting, global illumination and physical based shading.
							</li>
							<li>
								Able to produce quality lighting setup and solve performance challenges within the
								runtime constraints.
							</li>
							<li>Excellent problem-solving skills and the ability to adapt in using Unreal 5.</li>
							<li>Thorough knowledge of the game development process.</li>
							<li>Skilled in the use of Maya/Max and Photoshop.</li>
							<li>
								Good understanding of cinematography and the use of camera system technology in CG
								environments.
							</li>
						</ul>
