						<h4>Lead Concept Artist</h4>
						<p>
							The role of the lead concept artist is focused on visualizing concepts and ideas in
							such a way that it supports and enhances the vision of the art director as well as
							showing the development team critical aspects of each asset to reach alignment with
							both function and form.
						</p>
						<h4>Responsibilities:</h4>
						<ul>
							<li>
								Collaborate with our Art and Creative Directors to establish the aesthetic for each
								project.
							</li>
							<li>Craft clear style guides that direct team on creating compatible assets.</li>
							<li>Assign and monitor tasks attached to internal and external concept artists.</li>
							<li>
								Provide constructive feedback to internal staff and external partners, based on art
								direction and creative needs.
							</li>
							<li>
								Implement best practices and workflows to create a predictable and high-quality
								pipeline of concept art.
							</li>
							<li>
								Other responsibilities include storyboarding, branding, and website initiatives.
							</li>
						</ul>
						<h4>Requirements:</h4>
						<ul>
							<li>5 years of experience with video games and artist management.</li>
							<li>Ability to create 2D concepts that are instructive for 3D modeling.</li>
							<li>Comfortable working iteratively based on feedback and quality goals.</li>
							<li>
								Experience creating thumbnails, sketches, model sheets, and orthographic images.
							</li>
							<li>
								Excellent eye for color palette, lighting, shapes, and level of detail in service of
								character and environment compositions.
							</li>
							<li>
								Online portfolio. Please include development sketches and markups in addition to
								more refined, polished, and fully realized designs.
